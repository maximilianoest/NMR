function createDataSavingObject()
variablesInformation = evalin('base','whos');

variablesNotToSave = ["azimuthAngle","correlationFunction0W0" ...
    ,"correlationFunction1W0","correlationFunction2W0" ...
    ,"dataSavingObject","nearestNeighbourDistancesPow3" ...
    ,"nearestNeighboursX" ...
    ,"nearestNeighboursY","nearestNeighboursZ","polarAngle" ...
    ,"relativeX","relativeY","relativeZ","rotatedX","rotatedY" ...
    ,"rotatedZ","sphericalHarmonicFirstOrder" ...
    ,"sphericalHarmonicSecondOrder","sphericalHarmonicZerothOrder" ...
    ,"trajectoryX","trajectoryY","trajectoryZ"];

variablesCount = size(variablesInformation,1);

for variableNr = 1:variablesCount
    variable = variablesInformation(variableNr);
    if ~sum(variable.name == variablesNotToSave)
        evalin('base',['dataSavingObject.' variable.name ...
            '= ' variable.name ';'])
    end
    
end

end
